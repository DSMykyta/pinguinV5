/**
 * ╔══════════════════════════════════════════════════════════════════════════╗
 * ║                   TABLE GENERATOR - СТАН (STATE)                         ║
 * ╚══════════════════════════════════════════════════════════════════════════╝
 * * ПРИЗНАЧЕННЯ:
 * Управління внутрішнім станом генератора. Зберігає дані, які можуть змінюватися
 * під час роботи, наприклад, лічильник створених рядків.
 * Це забезпечує єдине джерело правди для стану модуля.
 */

export const state = {
    rowCounter: 1
};

/**
 * Збільшує лічильник рядків на 1 і повертає нове значення.
 * @returns {number} - Новий унікальний номер для рядка.
 */
export function incrementRowCounter() {
    return state.rowCounter++;
}

/**
 * Скидає лічильник рядків до початкового значення 1.
 */
export function resetRowCounter() {
    state.rowCounter = 1;
}